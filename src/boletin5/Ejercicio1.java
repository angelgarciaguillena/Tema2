package boletin5;

public class Ejercicio1 {
	public static void main(String[] args) {
		
		/*Creamos una variable para almacenar el numero*/
		int numero = 1;
		
		/*Creamos un bucle do while para imprimir los 20 primeros numeros naturales de forma que se vaya sumando al numero uno mas y se imprima el numero*/
		do {
			System.out.println(numero);
			numero++;
		} while(numero <= 20);
	}
}
